
public class DemoLoop {

	public static void main(String[] args) {
		// To repeat certain function we use loop.
		//================while Loop======================
		int i=1;
		while (i<=10) {
			System.out.println(i);
			i++;		//i++ means i=i+1; also i+=2 means i=i+2;
			//to print only odd or even number just start from 1odd/2even and at end increment 2;
		}
//-------------------TO print from 10-1/descending order---------------
		int x=10;
		while (x>=1) {
			System.out.println(x);
			x--;
		}
	}

}
